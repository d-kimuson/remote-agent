import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '../..');
const openapiPath = resolve(repoRoot, 'docs/tmp/openapi.json');
const outputPath = resolve(repoRoot, 'native/src/api/types.rs');
const openapi = JSON.parse(readFileSync(openapiPath, 'utf8'));

const required = (schema, name) => schema.required?.includes(name) === true;

const rustType = (schema) => {
  if (schema['x-rust-type'] !== undefined) {
    return schema['x-rust-type'];
  }
  if (schema.anyOf !== undefined) {
    const nonNull = schema.anyOf.filter((item) => item.type !== 'null');
    if (nonNull.length === 1 && nonNull.length !== schema.anyOf.length) {
      return `Option<${rustType(nonNull[0])}>`;
    }
    const literals = schema.anyOf
      .filter((item) => typeof item.const === 'string')
      .map((item) => `"${item.const}"`);
    if (literals.length === schema.anyOf.length) {
      return 'String';
    }
    return 'serde_json::Value';
  }

  if (typeof schema.const === 'string') {
    return 'String';
  }

  if (schema.type === 'array') {
    return `Vec<${rustType(schema.items ?? {})}>`;
  }
  if (schema.type === 'boolean') {
    return 'bool';
  }
  if (schema.type === 'number' || schema.type === 'integer') {
    return 'f64';
  }
  if (schema.type === 'object') {
    return 'serde_json::Value';
  }
  return 'String';
};

const fieldName = (name) => {
  if (name === 'type') {
    return 'kind';
  }
  return name.replace(/[A-Z]/g, (value) => `_${value.toLowerCase()}`);
};

const structFromSchema = (structName, schema) => {
  const properties = schema.properties ?? {};
  const fields = Object.entries(properties).map(([name, property]) => {
    const baseType = rustType(property);
    const ty =
      required(schema, name) || baseType.startsWith('Option<') ? baseType : `Option<${baseType}>`;
    const rustName = fieldName(name);
    const rename = rustName === name ? '' : `    #[serde(rename = "${name}")]\n`;
    return `${rename}    pub ${rustName}: ${ty},`;
  });

  return `#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]\npub struct ${structName} {\n${fields.join('\n')}\n}\n`;
};

const jsonSchema = (path, method, segment) => {
  const operation = openapi.paths[path][method];
  if (segment === 'request') {
    return operation.requestBody?.content?.['application/json']?.schema;
  }
  return operation.responses?.[segment]?.content?.['application/json']?.schema;
};

const nested = (schema, key) => schema.properties?.[key];

const sessionSchema = nested(jsonSchema('/api/acp/sessions', 'get', '200'), 'sessions').items;
const messageSchema = nested(
  jsonSchema('/api/acp/sessions/{sessionId}/messages', 'get', '200'),
  'messages',
).items;
const projectSchema = nested(jsonSchema('/api/projects', 'get', '200'), 'projects').items;
const providerSchema = nested(jsonSchema('/api/acp/providers', 'get', '200'), 'providers').items;
const appInfoSchema = jsonSchema('/api/info', 'get', '200');
const modelCatalogSchema = jsonSchema('/api/acp/agent/model-catalog', 'get', '200');
const slashCommandsSchema = jsonSchema('/api/acp/agent/slash-commands', 'get', '200');
const projectSettingsSchema = nested(
  jsonSchema('/api/projects/{projectId}/settings', 'get', '200'),
  'settings',
);

nested(appInfoSchema, 'agentPresets').items['x-rust-type'] = 'AgentPreset';
nested(sessionSchema, 'availableModes').items['x-rust-type'] = 'ModeOption';
nested(sessionSchema, 'availableModels').items['x-rust-type'] = 'ModelOption';
nested(providerSchema, 'preset')['x-rust-type'] = 'AgentPreset';
nested(modelCatalogSchema, 'availableModes').items['x-rust-type'] = 'ModeOption';
nested(modelCatalogSchema, 'availableModels').items['x-rust-type'] = 'ModelOption';
nested(slashCommandsSchema, 'commands').items['x-rust-type'] = 'SlashCommand';
nested(projectSettingsSchema, 'modelPreferences').items['x-rust-type'] = 'ProjectModelPreference';
nested(jsonSchema('/api/acp/sessions/{sessionId}/messages', 'post', '200'), 'session')[
  'x-rust-type'
] = 'SessionSummary';

const source = `// @generated by native/scripts/generate-openapi-rust-client.mjs from docs/tmp/openapi.json
// Do not edit by hand.

${structFromSchema('AgentPreset', nested(appInfoSchema, 'agentPresets').items)}
${structFromSchema('AppInfo', appInfoSchema)}
${structFromSchema('Project', projectSchema)}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct ProjectsResponse {
    pub projects: Vec<Project>,
}

${structFromSchema('ModeOption', nested(sessionSchema, 'availableModes').items)}
${structFromSchema('ModelOption', nested(sessionSchema, 'availableModels').items)}
${structFromSchema('SlashCommand', nested(slashCommandsSchema, 'commands').items)}
${structFromSchema('SessionSummary', sessionSchema)}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct SessionsResponse {
    pub sessions: Vec<SessionSummary>,
}

${structFromSchema('RawEvent', nested(messageSchema, 'rawEvents').items.anyOf.at(-1))}
${structFromSchema('ChatMessage', messageSchema)}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct SessionMessagesResponse {
    pub messages: Vec<ChatMessage>,
}

${structFromSchema('MessageRequest', jsonSchema('/api/acp/sessions/{sessionId}/messages', 'post', 'request'))}
${structFromSchema('CreateSessionRequest', jsonSchema('/api/acp/sessions', 'post', 'request'))}
${structFromSchema('PrepareSessionRequest', jsonSchema('/api/acp/agent/prepare', 'post', 'request'))}
${structFromSchema('PrepareSessionResponse', jsonSchema('/api/acp/agent/prepare', 'post', '202'))}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct SessionResponse {
    pub session: SessionSummary,
}

${structFromSchema('MessageResponse', jsonSchema('/api/acp/sessions/{sessionId}/messages', 'post', '200'))}
${structFromSchema('ProviderEntry', providerSchema)}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct AgentProvidersResponse {
    pub providers: Vec<ProviderEntry>,
}

${structFromSchema('AgentModelCatalogResponse', modelCatalogSchema)}
${structFromSchema('AgentSlashCommandsResponse', slashCommandsSchema)}
${structFromSchema('ProjectModelPreference', nested(projectSettingsSchema, 'modelPreferences').items)}
${structFromSchema('ProjectSettings', projectSettingsSchema)}
#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct ProjectSettingsResponse {
    pub settings: ProjectSettings,
}

#[derive(Debug, Clone, serde::Deserialize, serde::Serialize)]
pub struct ErrorResponse {
    pub error: String,
}
`;

writeFileSync(outputPath, source);
